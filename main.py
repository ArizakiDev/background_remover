def remove_background(image_path):
    if not os.path.exists(image_path):
        print(Colors.red + f"Erreur : Le fichier {image_path} n'existe pas." + Colors.reset)
        return
    
    try:
        original_image = Image.open(image_path)
        original_image.show()
        with open(image_path, "rb") as input_file:
            input_data = input_file.read()
        Write.Print("Suppression du fond en cours...\n", Colors.yellow, interval=0.05)
        output_data = remove(input_data)

        directory, filename = os.path.split(image_path)
        file_name_no_ext = os.path.splitext(filename)[0]

        if not directory:
            directory = os.getcwd()
        output_path = os.path.join(directory, f"{file_name_no_ext}_no_bg.png")

        with open(output_path, "wb") as output_file:
            output_file.write(output_data)

        Write.Print(f"Image sans fond sauvegardée à : {output_path}\n", Colors.green, interval=0.05)

        processed_image = Image.open(output_path)
        processed_image.show() 
    except Exception as e:
        Write.Print(f"Une erreur est survenue : {e}\n", Colors.red, interval=0.05)

